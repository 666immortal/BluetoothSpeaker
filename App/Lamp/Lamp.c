static void ConfigLamp(void);

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称: ConfigLamp
* 函数功能: 配置Lamp
* 输入参数: void 
* 输出参数: void
* 返 回 值: void
* 创建日期: 20XX年XX月XX日
* 注    意: 
*********************************************************************************************************/
static void ConfigLamp(void)
{
  InitPWM(2559, 0);
}

/*********************************************************************************************************
*                                              API函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称: InitLamp
* 函数功能: 初始化Lamp
* 输入参数: void 
* 输出参数: void
* 返 回 值: void
* 创建日期: 20XX年XX月XX日
* 注    意: 
*********************************************************************************************************/
void  InitLamp(void)
{
  ConfigLamp();
  
  TIM_SetCompare2(TIM3, 0);
  TIM_SetCompare3(TIM3, 0);
  TIM_SetCompare4(TIM3, 0); 
}

/*********************************************************************************************************
* 函数名称: SetLampBrightness
* 函数功能: 设置灯的亮度
* 输入参数: void 
* 输出参数: void
* 返 回 值: void
* 创建日期: 20XX年XX月XX日
* 注    意: 
*********************************************************************************************************/
void  SetLampBrightness(u8 mode)
{
  switch(mode)
  {
    case 0:                       //灯灭
      TIM_SetCompare2(TIM3, 0);
      TIM_SetCompare3(TIM3, 0);
      TIM_SetCompare4(TIM3, 0);           
      break;
    case 1:                       //一级亮度
      TIM_SetCompare2(TIM3, 853);
      TIM_SetCompare3(TIM3, 853);
      TIM_SetCompare4(TIM3, 853);      
      break;
    case 2:                       //二级亮度
      TIM_SetCompare2(TIM3, 1706);
      TIM_SetCompare3(TIM3, 1706);
      TIM_SetCompare4(TIM3, 1706);
      break;
    case 3:                       //三级亮度
      TIM_SetCompare2(TIM3, 2559);
      TIM_SetCompare3(TIM3, 2559);
      TIM_SetCompare4(TIM3, 2559); 
      break;
    case 4:                       //红
      TIM_SetCompare2(TIM3, 2559);
      TIM_SetCompare3(TIM3, 0);
      TIM_SetCompare4(TIM3, 0);
      break;
    case 5:                       //绿
      TIM_SetCompare2(TIM3, 0);
      TIM_SetCompare3(TIM3, 2559);
      TIM_SetCompare4(TIM3, 0);
      break;
    case 6:                       //蓝
      TIM_SetCompare2(TIM3, 0);
      TIM_SetCompare3(TIM3, 0);
      TIM_SetCompare4(TIM3, 2559);
      break;
    case 7:                       //黄
      TIM_SetCompare2(TIM3, 2599);
      TIM_SetCompare3(TIM3, 2599);
      TIM_SetCompare4(TIM3, 0);
      break;
    case 8:                       //粉
      TIM_SetCompare2(TIM3, 2599);
      TIM_SetCompare3(TIM3, 0);
      TIM_SetCompare4(TIM3, 2599);
      break;
    case 9:                       //青
      TIM_SetCompare2(TIM3, 0);
      TIM_SetCompare3(TIM3, 2599);
      TIM_SetCompare4(TIM3, 2599);
      break;
  }    
}

/*********************************************************************************************************
* 函数名称: SetLampColor
* 函数功能: 设置灯的颜色
* 输入参数: void 
* 输出参数: void
* 返 回 值: void
* 创建日期: 20XX年XX月XX日
* 注    意: 
*********************************************************************************************************/
void SetLampColor(u8 red, u8 green, u8 blue)
{
  TIM_SetCompare2(TIM3, red   * 10);
  TIM_SetCompare3(TIM3, green * 10);
  TIM_SetCompare4(TIM3, blue  * 10);
}
