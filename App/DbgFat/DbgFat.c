/*********************************************************************************************************
* 函数名称: mf_readdir
* 函数功能: 读取文件夹
* 输入参数: void 
* 输出参数: void
* 返 回 值: 执行结果
* 创建日期: 2018年03月01日
* 注    意: 
*********************************************************************************************************/
u8 mf_readdir(void)
{
	u8 res;
	char *fn;		
  
#if _USE_LFN
 	fileinfo.lfsize = _MAX_LFN * 2 + 1;
	fileinfo.lfname = MallocMemExt(USER_DEFINE_SRAMIN, fileinfo.lfsize);
#endif		  
	res = f_readdir(&dir, &fileinfo);//读取一个文件的信息
	if(res != FR_OK || fileinfo.fname[0] == 0)
	{
		FreeMemExt(USER_DEFINE_SRAMIN, fileinfo.lfname);
    
		return res;//读完了.
	}
#if _USE_LFN
	fn = *fileinfo.lfname ? fileinfo.lfname : fileinfo.fname;
#else
	fn = fileinfo.fname;
#endif	
	printf("\r\n DIR info:\r\n");

	printf("dir.id:%d\r\n", dir.id);
	printf("dir.index:%d\r\n", dir.index);
	printf("dir.sclust:%d\r\n", (int)dir.sclust);
	printf("dir.clust:%d\r\n", (int)dir.clust);
	printf("dir.sect:%d\r\n", (int)dir.sect);	  

	printf("\r\n");
	printf("File Name is:%s\r\n", fn);
	printf("File Size is:%d\r\n", (int)fileinfo.fsize);
	printf("File data is:%d\r\n", fileinfo.fdate);
	printf("File time is:%d\r\n", fileinfo.ftime);
	printf("File Attr is:%d\r\n", fileinfo.fattrib);
	printf("\r\n");
	FreeMemExt(USER_DEFINE_SRAMIN, fileinfo.lfname);
  
	return 0;
}			 

/*********************************************************************************************************
* 函数名称: mf_scan_files
* 函数功能: 遍历文件
* 输入参数: path:路径
* 输出参数: void
* 返 回 值: 执行结果
* 创建日期: 2018年03月01日
* 注    意: 
*********************************************************************************************************/
u8 mf_scan_files(u8 * path)
{
	FRESULT res;	  
  char *fn;   /* This function is assuming non-Unicode cfg. */
  
#if _USE_LFN
 	fileinfo.lfsize = _MAX_LFN * 2 + 1;
	fileinfo.lfname = MallocMemExt(USER_DEFINE_SRAMIN, fileinfo.lfsize);
#endif		  

  res = f_opendir(&dir, (const TCHAR*)path); //打开一个目录
  if(res == FR_OK) 
	{	
		printf("\r\n");
    
		while(1)
		{
	    res = f_readdir(&dir, &fileinfo);                   //读取目录下的一个文件
	    if (res != FR_OK || fileinfo.fname[0] == 0) 
      {
        break;  //错误了/到末尾了,退出
      }
	        //if (fileinfo.fname[0] == '.') continue;             //忽略上级目录
#if _USE_LFN
      fn = *fileinfo.lfname ? fileinfo.lfname : fileinfo.fname;
#else							   
      fn = fileinfo.fname;
#endif	                                              /* It is a file. */
      printf("%s/", path);//打印路径	
      printf("%s\r\n", fn);//打印文件名	  
		} 
  }	  
	FreeMemExt(USER_DEFINE_SRAMIN, fileinfo.lfname);
  
  return res;	  
}

/*********************************************************************************************************
* 函数名称: mf_showfree
* 函数功能: 显示剩余容量
* 输入参数: drv:盘符
* 输出参数: void
* 返 回 值: 剩余容量(字节)
* 创建日期: 2018年03月01日
* 注    意: 
*********************************************************************************************************/
u32 mf_showfree(u8 *drv)
{
	FATFS *fs1;
	u8 res;
  u32 fre_clust = 0, fre_sect = 0, tot_sect = 0;
  
  //得到磁盘信息及空闲簇数量
  res = f_getfree((const TCHAR*)drv, (DWORD*)&fre_clust, &fs1);
  
  if(res == 0)
	{											   
	  tot_sect = (fs1->n_fatent - 2) * fs1->csize;//得到总扇区数
	  fre_sect = fre_clust * fs1->csize;			//得到空闲扇区数	   
#if _MAX_SS != 512
		tot_sect *= fs1->ssize / 512;
		fre_sect *= fs1->ssize / 512;
#endif	  
		if(tot_sect < 20480)//总容量小于10M
		{
		    /* Print free space in unit of KB (assuming 512 bytes/sector) */
		    printf("\r\n磁盘总容量:%d KB\r\n"
		           "可用空间:%d KB\r\n",
		           tot_sect>>1, fre_sect>>1);
		}
    else
		{
		    /* Print free space in unit of KB (assuming 512 bytes/sector) */
		    printf("\r\n磁盘总容量:%d MB\r\n"
		           "可用空间:%d MB\r\n",
		           tot_sect>>11, fre_sect>>11);
		}
	}
  
	return fre_sect;
}		 

