/*********************************************************************************************************
* 函数名称: mf_read
* 函数功能: 读出数据
* 输入参数: len:读出的长度
* 输出参数: void
* 返 回 值: 执行结果
* 创建日期: 2018年03月01日
* 注    意: 
*********************************************************************************************************/
u8 mf_read(u16 len)
{
	u16 i, t;
	u8 res = 0;
	u16 tlen = 0;
  
	printf("\r\nRead file data is:\r\n");
  
	for(i = 0; i < len / 512; i++)
	{
		res = f_read(fnew, fatbuf ,512, &fnum);
    
		if(res)
		{
			printf("Read Error:%d\r\n", res);
      
			break;
		}
    else
		{
			tlen += fnum;
			for(t = 0; t < fnum; t++)printf("%c", fatbuf[t]); 
		}
	}
  
	if(len % 512)
	{
		res = f_read(fnew, fatbuf, len % 512, &fnum);
		if(res)	//读数据出错了
		{
			printf("\r\nRead Error:%d\r\n",res);   
		}
    else
		{
			tlen += fnum;
			for(t = 0; t < fnum; t++)
      {
        printf("%c", fatbuf[t]); 
      }
		}	 
	}
  
	if(tlen)
  {
    printf("\r\nReaded data len:%d\r\n", tlen);//读到的数据长度
  }
  
	printf("Read data over\r\n");	 
  
	return res;
}

