/*********************************************************************************************************
* 函数名称: InitSysTick
* 函数功能: 初始化SysTick  
* 输入参数: void  
* 输出参数: void  
* 返 回 值: void
* 创建日期: 2018年03月01日
* 注    意: SystemFrequency / 1000      1ms中断一次（计数1000次为1s，每计一次位1/1000s=1ms）
*           SystemFrequency / 4000      0.25ms中断一次（计数4000次为1s，每计一次位1/4000s=0.25ms）
*           SystemFrequency / 100000    10us中断一次（计数100000次为1s，每计一次位1/100000s=10us）
*           SystemFrequency / 1000000   1us中断一次（计数1000000次为1s，每计一次位1/1000000s=1us）
*********************************************************************************************************/
void InitSysTick( void )
{

  if (SysTick_Config(SystemCoreClock / 1000))  //配置系统滴答定时器1ms中断一次  
  { 
    while(1);                                  //错误发生的情况下，进入死循环
  }
}

