/*********************************************************************************************************
* 函数名称: TimDelayDec
* 函数功能: 延时计数  
* 输入参数: void
* 输出参数: void
* 返 回 值: void
* 创建日期: 2018年03月01日
* 注    意: 被SysTick中断服务函数SysTick_Handler()调用
*********************************************************************************************************/
static  void TimDelayDec(void)
{
  if (s_iTimDelayCnt != 0)   //延时计数器的数值不为0
  { 
    s_iTimDelayCnt--;        //延时计数器的数值减1
  }
}

/*********************************************************************************************************
* 函数名称: SysTick_Handler
* 函数功能: 延时计数中断服务函数 
* 输入参数: void
* 输出参数: void
* 返 回 值: void
* 创建日期: 2018年03月01日
* 注    意: 
*********************************************************************************************************/
void  SysTick_Handler(void)
{
  TimDelayDec();            //延时计数函数
}

